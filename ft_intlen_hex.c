#include "ft_printf.h"

int     ft_intlen_hex(unsigned int nb) 
{
        int     len;
    
        len = 0;
        if (nb == 0)
                return (1);
        while (nb)
        {
                nb /= 16; 
                len ++; 
        }
        return (len);
}

