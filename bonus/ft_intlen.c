#include "ft_printf.h"

int     ft_intlen(int nb) 
{
        int     len;

        len = 0;
        if (nb <= 0)
        {
                len += 1;
        }
        while (nb)
        {
                nb /= 10; 
                len ++; 
        }
        return (len);
}
