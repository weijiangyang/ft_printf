#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int		len;

	len = 0;
	if (!s)
		s = "(null)";
	while (*s)
	{
		ft_putchar (*s);
		s++;
		len++;
	}
	return (len);
}
