#include "ft_printf.h"

int	ft_putptr(void *ptr)
{
	int	count;

	if (!ptr)
		return (ft_putstr("(nil)"));
	count = 0;
	count += ft_putstr("0x");
	count += ft_puthex_ptr((unsigned long)ptr);
	return (count);
}
